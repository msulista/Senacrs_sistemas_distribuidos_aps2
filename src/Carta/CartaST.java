package Carta;

public class CartaST {
	
	private String nome;
	private int poder;
	private int força;
	private int agilizade;
	private int energia;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public int getPoder() {
		return poder;
	}
	public void setPoder(int poder) {
		this.poder = poder;
	}
	public int getForça() {
		return força;
	}
	public void setForça(int força) {
		this.força = força;
	}
	public int getAgilizade() {
		return agilizade;
	}
	public void setAgilizade(int agilizade) {
		this.agilizade = agilizade;
	}
	public int getEnergia() {
		return energia;
	}
	public void setEnergia(int energia) {
		this.energia = energia;
	}
	
}
